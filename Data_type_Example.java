import java.util.*;

class Data_type_Example{
public static void main(String[] args){
double a=100.0;
float b=33.0f;
int n=(int)(a/b);
switch(n){
case 3:
 System.out.println("3"+n);
 break;
case 33:
 System.out.println("33"+n);
 break;
case 100:
 System.out.println("100"+n);
 break;
default:
System.out.println("Invalid number");
}

}
}